package com.login.visualizationsoftware.model;

public class DataRecord {
    private String label;
    private int value;

    public DataRecord(String label, int value) {
        this.label = label;
        this.value = value;
    }

    public String getLabel() {
        return label;
    }

    public int getValue() {
        return value;
    }
}